package com.weparty.display.vo;

public class DisplayVO {
	
	private int display_no;
	private String display_link;
	private String display_poster;
	public int getDisplay_no() {
		return display_no;
	}
	public void setDisplay_no(int display_no) {
		this.display_no = display_no;
	}
	public String getDisplay_link() {
		return display_link;
	}
	public void setDisplay_link(String display_link) {
		this.display_link = display_link;
	}
	public String getDisplay_poster() {
		return display_poster;
	}
	public void setDisplay_poster(String display_poster) {
		this.display_poster = display_poster;
	}
	
	

}
